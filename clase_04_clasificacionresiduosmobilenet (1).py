def validate_and_correct_dataframe(df, image_index):
    df = df.copy()

    df["exists"] = df["filename"].isin(image_index)
    df["real_label_name"] = df["filename"].map(image_index)

    missing_count = int((~df["exists"]).sum())

    df = df[df["exists"]].copy()
    df["label_name"] = df["real_label_name"]
    df["label"] = df["label_name"].map(inv_label_map).astype(int)

    df["filepath"] = df.apply(
        lambda row: str(IMAGE_BASE_PATH / row["label_name"] / row["filename"]),
        axis=1
    )

    df = df.drop(columns=["exists", "real_label_name"])

    return df, missing_count

