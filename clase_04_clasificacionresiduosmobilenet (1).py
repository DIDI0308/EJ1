def build_image_index(image_base_path):
    image_index = {}

    if not image_base_path.exists():
        raise FileNotFoundError(f"No existe la carpeta de imágenes: {image_base_path}")

    for class_folder in sorted(image_base_path.iterdir()):
        if class_folder.is_dir():
            label_name = class_folder.name
            for file_path in class_folder.iterdir():
                if file_path.is_file():
                    image_index[file_path.name] = label_name

    return image_index

